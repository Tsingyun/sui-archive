def _write_default_404(output_dir, site_name, site_url, build_info_text):
    """Write a minimal 404 page when no template is available."""
    content = f"""<!DOCTYPE html>
<html lang="zh-CN">
<head>
<meta charset="UTF-8">
<meta name="viewport" content="width=device-width, initial-scale=1.0">
<title>404 - 页面未找到 - {html.escape(site_name)}</title>
<link rel="icon" href="/assets/favicon.svg" type="image/svg+xml">
<link rel="stylesheet" href="/style.css">
{build_info_text}
</head>
<body>
<main style="text-align:center;padding:4rem 1rem;">
<h1>404</h1>
<p>抱歉，你访问的页面不存在。</p>
<p><a href="/">返回首页</a> · <a href="/search">搜索</a></p>
</main>
<script type="module" src="/js/app.js"></script>
</body>
</html>"""
    (Path(output_dir) / "404.html").write_text(content, encoding="utf-8")


