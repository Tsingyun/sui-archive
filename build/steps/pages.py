def _read_partial(partials_dir, filename):
    """Read a partial HTML file, returning empty string if not found."""
    path = Path(partials_dir) / filename
    if path.is_file():
        return path.read_text(encoding="utf-8")
    return ""


def _truncate_text(text, max_length):
    """Truncate text to max_length at the last sentence boundary.

    Sentence boundaries: 。！？ or newline. Appends … if truncated.
    Avoids breaking inside [emoji_name] brackets.
    """
    if not text or len(text) <= max_length:
        return text or ""

    truncated = text[:max_length]

    # Find the last sentence-ending punctuation or newline
    last_break = -1
    for i in range(len(truncated) - 1, -1, -1):
        ch = truncated[i]
        if ch in "。！？\n":
            last_break = i
            break

    if last_break > max_length // 2:
        # Truncate at last sentence boundary
        return truncated[: last_break + 1] + "…"

    # No good sentence break found; hard truncate
    return truncated.rstrip() + "…"
