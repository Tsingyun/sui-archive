"""
Step 6: pages — HTML page generation.

Reads HTML templates from src/html/, injects build info and config values,
generates detail page stubs with full SEO metadata for every post, and
copies the 404 page.
"""

import html
import json
import os
import re
from datetime import datetime, timezone
from pathlib import Path


# ---------------------------------------------------------------------------
# Static page names (template file name without extension -> output file name)
# ---------------------------------------------------------------------------
STATIC_PAGES = [
    "index",
    "search",
    "gallery",
    "timeline",
    "stats",
    "tags",
    "about",
]


def run(db, config, output_dir, logger):
    """Generate all HTML pages for the archive.

    Args:
        db: sqlite3.Connection with row_factory=Row.
        config: dict from config.yaml.
        output_dir: Path to the build output directory.
        logger: Logger with .step()/.info()/.warn()/.success() methods.
    """
    logger.step("Step 6: pages — Generating HTML pages")

    output_dir = Path(output_dir)
    project_root = Path(config.get("_project_root", "."))
    html_dir = project_root / "src" / "html"
    partials_dir = html_dir / "partials"

    site_config = config.get("site", {})
    site_name = site_config.get("name", "SUI Archive")
    site_url = site_config.get("url", "")
    site_description = site_config.get("description", "")

    build_timestamp = datetime.now(timezone.utc).strftime("%Y-%m-%dT%H:%M:%SZ")
    build_info_text = (
        f"<!-- Generated by SUI Archive Build Pipeline at {build_timestamp} -->"
    )

    # Load partials (header, footer, etc.)
    header_html = _read_partial(partials_dir, "header.html")
    footer_html = _read_partial(partials_dir, "footer.html")
    head_html = _read_partial(partials_dir, "head.html")

    static_count = 0
    detail_count = 0

    # -----------------------------------------------------------------------
    # 1. Static pages from templates
    # -----------------------------------------------------------------------
    for page_name in STATIC_PAGES:
        template_path = html_dir / f"{page_name}.html"
        if not template_path.is_file():
            logger.warn(f"Template not found: {template_path.name} — skipping")
            continue

        content = template_path.read_text(encoding="utf-8")

        # Replace placeholders
        content = content.replace("<!-- BUILD_INFO -->", build_info_text)
        content = content.replace("{{SITE_NAME}}", site_name)
        content = content.replace("{{SITE_URL}}", site_url)
        content = content.replace("{{SITE_DESCRIPTION}}", site_description)
        content = content.replace("<!-- HEADER -->", header_html)
        content = content.replace("<!-- FOOTER -->", footer_html)
        if head_html:
            content = content.replace("<!-- HEAD -->", head_html)

        out_path = output_dir / f"{page_name}.html"
        out_path.write_text(content, encoding="utf-8")
        static_count += 1

    # -----------------------------------------------------------------------
    # 2. 404 page
    # -----------------------------------------------------------------------
    not_found_path = html_dir / "404.html"
    if not_found_path.is_file():
        content = not_found_path.read_text(encoding="utf-8")
        content = content.replace("<!-- BUILD_INFO -->", build_info_text)
        content = content.replace("{{SITE_NAME}}", site_name)
        content = content.replace("{{SITE_URL}}", site_url)
        (output_dir / "404.html").write_text(content, encoding="utf-8")
        static_count += 1
    else:
        # Generate a minimal 404 page if no template exists
        _write_default_404(output_dir, site_name, site_url, build_info_text)
        static_count += 1

    # -----------------------------------------------------------------------
    # 3. Detail pages for each post
    # -----------------------------------------------------------------------
    pages_config = config.get("pages", {})
    generate_detail = pages_config.get("generate_detail_pages", True)

    if generate_detail:
        detail_count = _generate_detail_pages(
            db, config, output_dir, site_name, site_url, site_description,
            build_info_text, header_html, footer_html, logger,
        )

    logger.success(
        f"Generated {static_count} static pages, {detail_count} detail pages"
    )


# ---------------------------------------------------------------------------
# Detail page generation
# ---------------------------------------------------------------------------

def _generate_detail_pages(
    db, config, output_dir, site_name, site_url, site_description,
    build_info_text, header_html, footer_html, logger,
):
    """Generate a detail HTML stub for every post in the database."""

    output_dir = Path(output_dir)
    d_dir = output_dir / "d"
    d_dir.mkdir(parents=True, exist_ok=True)

    # Query all posts with their platform info and stats
    posts = db.execute("""
        SELECT
            p.id, p.uuid, p.platform_post_id, p.post_type,
            p.published_at, p.source_url, p.plain_text,
            p.repost_snapshot, p.original_author_id,
            pl.key AS platform_key, pl.display_name AS platform_name,
            a.display_name AS repost_author_name,
            a.profile_url AS repost_author_url
        FROM posts p
        JOIN platforms pl ON p.platform_id = pl.id
        LEFT JOIN authors a ON p.original_author_id = a.id
        WHERE p.is_deleted = 0
        ORDER BY p.published_at ASC
    """).fetchall()

    # Build a lookup for prev/next navigation
    post_ids = [row["platform_post_id"] for row in posts]

    count = 0
    for i, post in enumerate(posts):
        uuid = post["uuid"]
        pid = post["platform_post_id"]
        plain_text = post["plain_text"] or ""
        published_at = post["published_at"] or ""
        source_url = post["source_url"] or ""
        post_type = post["post_type"] or "text"
        platform_name = post["platform_name"] or ""

        # Get images for this post
        images = db.execute("""
            SELECT i.filename, i.width, i.height, i.file_size,
                   i.mime_type, i.is_cover, pm.is_repost_media
            FROM post_media pm
            JOIN images i ON pm.image_id = i.id
            WHERE pm.post_id = ?
            ORDER BY pm.sort_order
        """, (post["id"],)).fetchall()

        # Get latest stats for this post
        stats_row = db.execute("""
            SELECT likes, comments, forwards, views
            FROM post_stats
            WHERE post_id = ?
            ORDER BY snapshot_at DESC LIMIT 1
        """, (post["id"],)).fetchone()

        likes = stats_row["likes"] if stats_row else 0
        comments = stats_row["comments"] if stats_row else 0
        forwards = stats_row["forwards"] if stats_row else 0

        # Get tags
        tags = db.execute("""
            SELECT t.name, t.slug, t.color
            FROM post_tags pt
            JOIN tags t ON pt.tag_id = t.id
            WHERE pt.post_id = ?
        """, (post["id"],)).fetchall()

        # Prev / next navigation
        prev_pid = post_ids[i - 1] if i > 0 else None
        next_pid = post_ids[i + 1] if i < len(post_ids) - 1 else None

        # Build the text preview for SEO
        text_preview = _truncate_text(plain_text, 200)
        text_short = _truncate_text(plain_text, 100)
        title_text = text_preview if text_preview else f"动态 {pid}"

        # Cover image for OG tags
        cover_image = None
        for img in images:
            if img["is_cover"]:
                cover_image = img
                break
        if cover_image is None and images:
            cover_image = images[0]

        # Build OG image URL
        og_image_url = ""
        og_image_width = ""
        og_image_height = ""
        if cover_image and cover_image["width"] and cover_image["height"]:
            stem = Path(cover_image["filename"]).stem
            og_filename = f"{stem}_w600.webp"
            og_image_url = f"{site_url}/thumbs/{og_filename}"
            # Calculate height at 600px width
            if cover_image["width"] > 600:
                ratio = 600 / cover_image["width"]
                h = int(cover_image["height"] * ratio)
                og_image_width = "600"
                og_image_height = str(h)
            else:
                og_image_width = str(cover_image["width"])
                og_image_height = str(cover_image["height"])
        else:
            og_image_url = f"{site_url}/assets/og-image.png"

        page_url = f"{site_url}/d/{pid}"
        escaped_title = html.escape(title_text)
        escaped_desc = html.escape(text_short)
        escaped_full_text = html.escape(plain_text)

        # Parse published date for Schema.org
        date_published = published_at
        # Tags as JSON-LD keywords
        tag_names = [t["name"] for t in tags] if tags else []

        # Build image list for Schema.org
        schema_images = []
        if cover_image:
            stem = Path(cover_image["filename"]).stem
            schema_images.append(f"{site_url}/thumbs/{stem}_w600.webp")

        # JSON-LD structured data
        jsonld = {
            "@context": "https://schema.org",
            "@type": "SocialMediaPosting",
            "headline": title_text,
            "datePublished": date_published,
            "author": {
                "@type": "Person",
                "name": "岁己SUI",
            },
            "url": page_url,
            "interactionStatistic": [
                {
                    "@type": "InteractionCounter",
                    "interactionType": "Like",
                    "userInteractionCount": likes,
                },
                {
                    "@type": "InteractionCounter",
                    "interactionType": "Comment",
                    "userInteractionCount": comments,
                },
                {
                    "@type": "InteractionCounter",
                    "interactionType": "Share",
                    "userInteractionCount": forwards,
                },
            ],
        }
        if schema_images:
            jsonld["image"] = schema_images
        if tag_names:
            jsonld["keywords"] = tag_names

        jsonld_str = html.escape(
            json.dumps(jsonld, ensure_ascii=False, indent=2),
            quote=True,
        )

        # Build the HTML
        detail_html = _render_detail_page(
            site_name=site_name,
            site_url=site_url,
            site_description=site_description if not text_short else text_short,
            page_url=page_url,
            pid=pid,
            uuid=uuid,
            title=escaped_title,
            description=escaped_desc,
            og_image_url=og_image_url,
            og_image_width=og_image_width,
            og_image_height=og_image_height,
            date_published=date_published,
            jsonld=jsonld_str,
            full_text=escaped_full_text,
            platform_name=platform_name,
            source_url=source_url,
            prev_pid=prev_pid,
            next_pid=next_pid,
            header_html=header_html,
            footer_html=footer_html,
            build_info_text=build_info_text,
        )

        # Write to output_dir/d/{platform_post_id}/index.html
        post_dir = d_dir / pid
        post_dir.mkdir(parents=True, exist_ok=True)
        (post_dir / "index.html").write_text(detail_html, encoding="utf-8")
        count += 1

    return count


# ---------------------------------------------------------------------------
# Detail page HTML template
# ---------------------------------------------------------------------------

def _render_detail_page(
    site_name, site_url, site_description, page_url, pid, uuid,
    title, description, og_image_url, og_image_width, og_image_height,
    date_published, jsonld, full_text, platform_name, source_url,
    prev_pid, next_pid, header_html, footer_html, build_info_text,
):
    """Render a minimal detail page HTML stub with full SEO metadata."""

    prev_link = f'<a href="/d/{prev_pid}" rel="prev">&larr; 上一条</a>' if prev_pid else ""
    next_link = f'<a href="/d/{next_pid}" rel="next">下一条 &rarr;</a>' if next_pid else ""
    source_link = (
        f'<a href="{html.escape(source_url)}" target="_blank" rel="noopener">'
        f'在{html.escape(platform_name)}上查看原始动态</a>'
        if source_url else ""
    )

    return f"""<!DOCTYPE html>
<html lang="zh-CN">
<head>
<meta charset="UTF-8">
<meta name="viewport" content="width=device-width, initial-scale=1.0">
<title>{title} - {html.escape(site_name)}</title>
<meta name="description" content="{description}">
<link rel="canonical" href="{page_url}">

<!-- Open Graph -->
<meta property="og:type" content="article">
<meta property="og:title" content="{title}">
<meta property="og:description" content="{description}">
<meta property="og:url" content="{page_url}">
<meta property="og:image" content="{og_image_url}">
<meta property="og:image:width" content="{og_image_width}">
<meta property="og:image:height" content="{og_image_height}">
<meta property="og:site_name" content="{html.escape(site_name)}">
<meta property="og:locale" content="zh_CN">
<meta property="article:published_time" content="{date_published}">

<!-- Twitter Card -->
<meta name="twitter:card" content="summary_large_image">
<meta name="twitter:title" content="{title}">
<meta name="twitter:description" content="{description}">
<meta name="twitter:image" content="{og_image_url}">

<!-- Schema.org JSON-LD -->
<script type="application/ld+json">{jsonld}</script>

<!-- Favicon -->
<link rel="icon" href="/assets/favicon.svg" type="image/svg+xml">
<link rel="icon" href="/assets/favicon.ico" type="image/x-icon">
<link rel="apple-touch-icon" href="/assets/apple-touch-icon.png">

<!-- Stylesheet (hash injected by assets step) -->
<link rel="stylesheet" href="/style.css">

{build_info_text}
</head>
<body>
{header_html}
<main id="post-detail" data-uuid="{uuid}" data-platform-post-id="{pid}">
<nav class="breadcrumb" aria-label="面包屑">
<a href="/">首页</a> &rsaquo; <span>动态 {pid}</span>
</nav>
<article>
<time datetime="{date_published}">{date_published}</time>
<div class="post-text">{full_text}</div>
<div id="post-images" class="post-images" aria-label="图片"></div>
<div class="post-stats">
<span>{platform_name}</span>
</div>
{source_link}
</article>
<nav class="post-nav">
{prev_link}
{next_link}
</nav>
<div id="share-panel"></div>
</main>
{footer_html}
<script type="module" src="/js/app.js"></script>
</body>
</html>"""


